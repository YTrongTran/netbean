    // phan combobox va filldata
    public void filldata() {
        DefaultTableModel tbmodel = (DefaultTableModel) tabbang.getModel();
        tbmodel.setRowCount(0);//xoa data trong table
        for (QuanLyNV nv : dao.getALLQuanLY()) {
            Object rowdata[] = new Object[5];
            rowdata[0] = nv.getMaNV();
            rowdata[1] = nv.getTenNV();
            //ngay sinh hien thi 
            rowdata[2] = date_format.format(nv.getNgaySinh());
            //gioi tinh hien thij nam/nu
            String gt = "Nu";
            if (nv.isGioiTinh()) {
                gt = "Nam";
            }
            rowdata[3] = gt;
            rowdata[4] = nv.getPhongBan();
            tbmodel.addRow(rowdata);
        }
    }

