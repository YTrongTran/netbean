    private void btnsaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsaveActionPerformed
        // TODO add your handling code here:
        if (checkValidate()) {
            try {
                QuanLyNV nv = getModel();
                if (dao.add(nv) > 0) {
                    JOptionPane.showMessageDialog(this, "BAN DA LUU THANH CONG");
                    lblstatus.setText("BAN DA LUU THANH CONG");
                    lblstatus.setForeground(Color.blue);
                    lblCount.setText("COUNT:" + dao.count());
                    //cap nhat thanh cong load data len table
                    filldata();
                } else {
                    lblstatus.setText("BAN DA LUU KHONG THANH CONG");
                    lblstatus.setForeground(Color.red);
                }
            } catch (ParseException ex) {
                System.out.println("Error:" + ex.toString());;
            }

        } else {
            JOptionPane.showMessageDialog(this, "BAN LUU THAT BAI");
        }
    }//GEN-LAST:event_btnsaveActionPerformed

    private void btneditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btneditActionPerformed
        // TODO add your handling code here:
        if (txtmanv.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "BAN CHUA CAP NHAT NHAN VIEN");

        } else {
            try {
                QuanLyNV nv = getModel();
                if (dao.edit(nv) > 0) {
                    lblstatus.setText("BAN DA CAP NHAT THANH CONG");
                    lblstatus.setForeground(Color.blue);
                    filldata();
                } else {

                    lblstatus.setText("BAN KHONG TIM THAY");
                    lblstatus.setForeground(Color.red);
                }
            } catch (ParseException ex) {
                System.out.println("Error:" + ex.toString());;
            }
        }
    }//GEN-LAST:event_btneditActionPerformed

    private void btnxoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnxoaActionPerformed
        // TODO add your handling code here:
        if (dao.del(txtmanv.getText()) > 0) {
            JOptionPane.showMessageDialog(this, "BAN XOA THANH CONG");
            lblCount.setText("COUNT:" + dao.count());
            filldata();
        } else {
            lblstatus.setText("KHONG TIM THAY");
            lblstatus.setForeground(Color.red);

        }
    }//GEN-LAST:event_btnxoaActionPerformed

    private void btnfindActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnfindActionPerformed
        // TODO add your handling code here:
        if (txtmanv.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "BAN CHUA NHAP MA TIM KIEM ");
        } else {
            QuanLyNV nv = dao.findBYID(txtmanv.getText());
            if (nv != null) {
                setModel(nv);
            } else {
                lblstatus.setText("KHONG TIM THAY NHAN VIEN" + txtmanv.getText());
                lblstatus.setForeground(Color.red);
            }

        }
    }//GEN-LAST:event_btnfindActionPerformed

