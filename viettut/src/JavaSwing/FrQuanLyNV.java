    public void seret() {
        txtmanv.setText("");
        txttennv.setText("");
        txtngaysinh.setText("");
        rdNam.setSelected(true);
        cboPhongban.setSelectedIndex(0);
    }
// Phan SAVE

    public QuanLyNV getModel() throws ParseException {
        QuanLyNV nv = new QuanLyNV();
        nv.setMaNV(txtmanv.getText());
        nv.setTenNV(txttennv.getText());
        nv.setNgaySinh(date_format.parse(txtngaysinh.getText()));
        boolean gt = false;
        if (rdNam.isSelected()) {
            gt = true;
        }
        nv.setGioiTinh(gt);
        nv.setPhongBan(cboPhongban.getSelectedItem().toString());
        return nv;
    }

    public boolean checkValidate() {
        if (txtmanv.getText().isEmpty()
                || txttennv.getText().isEmpty()
                || txtngaysinh.getText().isEmpty()) {
            return false;
        }
        return true;
    }

    public void setModel(QuanLyNV nv) {
        txtmanv.setText(nv.getMaNV());
        txttennv.setText(nv.getTenNV());
        txtngaysinh.setText(date_format.format(nv.getNgaySinh()));
        rdNam.setSelected(nv.isGioiTinh());
        rdNu.setSelected(!nv.isGioiTinh());
        cboPhongban.setSelectedItem(nv.getPhongBan());
    }

