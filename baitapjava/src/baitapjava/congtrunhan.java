public boolean validateFrom(){
    if (txtsoa.getText().isEmpty()||txtsob.getText().isEmpty()) {
        return false;
    }
return true;
}
    private void btntinhActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btntinhActionPerformed
        // TODO add your handling code here:
        if (validateFrom()) {
            double soa= Double.parseDouble(txtsoa.getText());
            double sob= Double.parseDouble(txtsob.getText());
            double result =0;
            if (rdcong.isSelected()) {
                result=soa+sob;
            }
            if (rdtru.isSelected()) {
                result = soa-sob;
                
            }
            if (rdnhan.isSelected()) {
                result = soa*sob;
                
            }
            if (rdchia.isSelected()) {
                if (sob==0) {
                    JOptionPane.showMessageDialog(this, "loi chia cho 0");
                } else {
                     result = soa/sob;
                }
               
            }
            lblresult.setText("ket qua:"+result);
        }
        else{
        JOptionPane.showMessageDialog(this, "ban chua nhap gia tri");
        
        }
    }//GEN-LAST:event_btntinhActionPerformed

