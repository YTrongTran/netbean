    public void setModel(QuanLySach ql) {
        txttensach.setText(ql.getTenSach());
        txtmasach.setText(Integer.toString(ql.getMaSach()));
        txtgia.setText(Double.toString(ql.getGia()));
        txtngaynhap.setText(date.format(ql.getNgayNhap()));
        cbonhaxuatban.setSelectedItem(ql.getNhaXuatBan());
    }
    private void tabListMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabListMouseClicked
        // TODO add your handling code here:
        QuanLySach hd = new QuanLySach();
        int position = tabList.rowAtPoint(evt.getPoint());
        txtmasach.setText(tabList.getValueAt(position, 0).toString());
        txttensach.setText(tabList.getValueAt(position, 1).toString());
        txtgia.setText(tabList.getValueAt(position, 2).toString());
        txtngaynhap.setText(tabList.getValueAt(position, 3).toString());
        cbonhaxuatban.setSelectedIndex(daox.findnhaXuatban(tabList.getValueAt(position, 4).toString()));

    }//GEN-LAST:event_tabListMouseClicked

    private void tabListMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabListMousePressed
        // TODO add your handling code here:
        if (evt.getClickCount() == 2) {
            int rowID = tabList.rowAtPoint(evt.getPoint());
            txtmasach.setText(tabList.getValueAt(rowID, 0).toString());
            txttensach.setText(tabList.getValueAt(rowID, 1).toString());
            txtgia.setText(tabList.getValueAt(rowID, 2).toString());
            txtngaynhap.setText(tabList.getValueAt(rowID, 3).toString());
            cbonhaxuatban.setSelectedIndex(daox.findnhaXuatban(tabList.getValueAt(rowID, 4).toString()));
        }
    }//GEN-LAST:event_tabListMousePressed

