    public QuanLySach getModel() throws ParseException {
        QuanLySach q = new QuanLySach();
        NhaXuatBan n = (NhaXuatBan) cbonhaxuatban.getSelectedItem();
        q.setNhaXuatBan(n);
        q.setMaSach(Integer.parseInt(txtmasach.getText()));
        q.setTenSach(txttensach.getText());
        q.setGia(Double.parseDouble(txtgia.getText()));

        q.setNgayNhap(date.parse(txtngaynhap.getText()));
        return q;
    }

    public void filldata() {
        DefaultTableModel model = (DefaultTableModel) tabList.getModel();
        model.setRowCount(0);
        for (QuanLySach q : daos.getAllQuanLySach()) {
            Object rowdata[] = new Object[5];
            rowdata[0] = q.getMaSach();
            System.out.println("--1--");

            rowdata[1] = q.getTenSach();
            System.out.println("--2--");
            rowdata[2] = q.getGia();
            System.out.println("---3-");

            rowdata[3] = date.format(q.getNgayNhap());
            int p = daox.findnhan(q.getNhaXuatBan().getMaXB());

            rowdata[4] = daox.getAllNhaXuatBan().get(p).getTenXB();
            System.out.println("--5--");

            model.addRow(rowdata);
        }
    }
    private void btnluuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnluuActionPerformed
        // TODO add your handling code here:
        if (txtmasach.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Yeu cau ban nhap day du");
        } else {
            try {
                QuanLySach q = getModel();
                if (daos.add(q) > 0) {
                    filldata();
                    JOptionPane.showMessageDialog(this, "ban them thanh cong");
                } else {
                    JOptionPane.showMessageDialog(this, "ban them that bai");
                }
            } catch (ParseException ex) {
                Logger.getLogger(QuanLySachDia.class.getName()).log(Level.SEVERE, null, ex);
            }

        }

    }//GEN-LAST:event_btnluuActionPerformed

