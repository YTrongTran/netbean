    public void fillData() {
        DefaultTableModel tbModel = (DefaultTableModel) tabbang.getModel();
        tbModel.setRowCount(0);// clear table
        for (FrSachclass cs : frdao.getALLSach()) {
            Object rowData[] = new Object[8];
            rowData[0] = cs.getMaSach();
            rowData[1] = cs.getTenSach();
            rowData[2] = cs.getChuyenNganh();
            rowData[3] = date.format(cs.getNgayNhap());
            rowData[4] = cs.getSoLuong();
            rowData[5] = cs.getGiaBan();
            rowData[6] = cs.getSoLuong() * cs.getGiaBan();
            rowData[7] = cs.getHinhAnh();
            tbModel.addRow(rowData);

        }

    }

    
    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        if (vailidataFrom()) {
            try {
                FrSachclass sl = getModell();
                if (frdao.add(sl) > 0) {
                    JOptionPane.showMessageDialog(this, "LUU THANH CONG");
                    fillData();
                }
            } catch (ParseException ex) {
                Logger.getLogger(FrSach.class.getName()).log(Level.SEVERE, null, ex);
            }

        } else {
            JOptionPane.showMessageDialog(this, "BAN CHUA NHAP DAY DU GIU LIEU");
        }


    }//GEN-LAST:event_btnSaveActionPerformed

    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        // TODO add your handling code here:
        if (txtmaSach.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Ban chua nhap ma");

        } else {
            if (frdao.delFrSachById(txtmaSach.getText().trim()) > 0) {
                lblStatus.setText("DA XOA THANH CONG");
                lblStatus.setForeground(Color.BLUE);
                fillData();

            } else {
                lblStatus.setText("Khong tim thay ma Sach");
                lblStatus.setForeground(Color.RED);
            }
        }
    }//GEN-LAST:event_btnXoaActionPerformed

    public void setModel(FrSachclass s) {
        int rowdata= tabbang.getSelectedRow();
        txtmaSach.setText(s.getMaSach());
        txttenSach.setText(s.getTenSach());
        txtngayNhap.setText(date.format(s.getNgayNhap()));
        txtsoLuong.setText(s.getSoLuong() + "");
        txtgiaBan.setText(s.getGiaBan() + "");
        cbochuyenNganh.setSelectedItem(s.getChuyenNganh());
        //hinh anh
        if (s.getHinhAnh().equalsIgnoreCase("NO AVATA")) {
            lblLogo.setText("NO AVATA");
            lblLogo.setIcon(null);
        } else {
            lblLogo.setText("");
            ImageIcon imageIcon = new ImageIcon(getClass().getResource("/Images/"+tabbang.getValueAt(rowdata, 7).toString()));
            Image img = imageIcon.getImage();
            img.getScaledInstance(lblLogo.getWidth(), lblLogo.getHeight(), 0);
            lblLogo.setIcon(imageIcon);
        }
    }
