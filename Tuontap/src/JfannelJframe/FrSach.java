    public boolean vailidataFrom() {
        if (txttenSach.getText().isEmpty() || txtmaSach.getText().isEmpty()
                || txtngayNhap.getText().isEmpty() || txtsoLuong.getText().isEmpty() || txtgiaBan.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "ban can nhap thong tin day du");
           
            return false;
        }
        int soluong = Integer.parseInt(txtsoLuong.getText());
        double giaban = Double.parseDouble(txtgiaBan.getText());
        if (soluong<0) {
            JOptionPane.showMessageDialog(this, "Can nhap dung so luong");
            txtsoLuong.setBackground(Color.red);
            return false;
        }
        if (giaban<0) {
            JOptionPane.showMessageDialog(this, "Can nhap dung so luong");
            txtgiaBan.setBackground(Color.red);
        return false;
        }
        return true;
    }

    public FrSachclass getModell() throws ParseException {
        FrSachclass s = new FrSachclass();
        s.setMaSach(txtmaSach.getText().trim());
        s.setTenSach(txttenSach.getText().trim());
        s.setNgayNhap(date.parse(txtngayNhap.getText().trim()));
        s.setChuyenNganh((Chuyennganh) cbochuyenNganh.getSelectedItem());
        s.setSoLuong(Integer.parseInt(txtsoLuong.getText().trim()));
        s.setGiaBan(Double.parseDouble(txtgiaBan.getText().trim()));
        if (strHinhAnh == null) {
            s.setHinhAnh("NO AVATA");
        } else {
            s.setHinhAnh(strHinhAnh);
        }
        return s;
    }

