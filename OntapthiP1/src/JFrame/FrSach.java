    public boolean validataF() {
        if (txtmSach.getText().isEmpty() || txttenSach.getText().isEmpty() || txtngayNhap.getText().isEmpty() || txtgiaBan.getText().isEmpty()
                || txtsoLuong.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "BAN CAN NHAP THONG TIN DAY DU");
            return false;
        }
        int soluong = Integer.parseInt(txtsoLuong.getText());
        double giaban = Double.parseDouble(txtgiaBan.getText());
        if (soluong < 0) {
            JOptionPane.showMessageDialog(this, "YEU CAU NHAP LAI SO LUONH");
            txtsoLuong.setBackground(Color.red);
            return false;
        }
        if (giaban < 0) {
            JOptionPane.showMessageDialog(this, "YEU CAU NHAP LAI GIA BAN");
            txtgiaBan.setBackground(Color.red);
            return false;
        }
        return true;
    }
