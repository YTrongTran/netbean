    private void btnEDITActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEDITActionPerformed
        // TODO add your handling code here:
        if (txtmSach.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "BAN CHUA NHAP MA DAY DU");
        } else {
            try {
                Sachfr r = getModel();
                if (daos.editS(r) > 0) {
                    lblstatus.setText("BAT DAU SUA LAI THANH CONG");
                    lblstatus.setForeground(Color.blue);
                    filldata();
                } else {
                    lblstatus.setText("KHONG TIM THAY TEN DE SUA");
                    lblstatus.setForeground(Color.red);
                }
            } catch (ParseException ex) {
                System.out.println("EROOR" + ex.toString());
            }

        }


    }//GEN-LAST:event_btnEDITActionPerformed

