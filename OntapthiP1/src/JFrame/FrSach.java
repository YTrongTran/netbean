    public void filldata() {
        DefaultTableModel tb = (DefaultTableModel) tbbang.getModel();
        tb.setRowCount(0);//xoa bang
        for (Sachfr s : daos.getALLSACHFr()) {
            Object rowdata[] = new Object[8];
            rowdata[0] = s.getmSach();
            rowdata[1] = s.gettSach();
            rowdata[2] = s.getChuyennganhS();
            rowdata[3] = date_Format.format(s.getNgaynhap());
            rowdata[4] = s.getSoluong();
            rowdata[5] = s.getGiaban();
            rowdata[6] = s.getSoluong() * s.getGiaban();
            rowdata[7] = s.getHinhanh();
            tb.addRow(rowdata);
        }

    }

