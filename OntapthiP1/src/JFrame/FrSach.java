    public Sachfr getModel() throws ParseException {
        Sachfr r = new Sachfr();
        r.setmSach(txtmSach.getText().trim());
        r.settSach(txttenSach.getText().trim());
        r.setChuyennganhS((ChuyennganhS) cbochuyenNganhs.getSelectedItem());
        r.setNgaynhap(date_Format.parse(txtngayNhap.getText().trim()));
        r.setGiaban(Double.parseDouble(txtgiaBan.getText().trim()));
        r.setSoluong(Integer.parseInt(txttenSach.getText().trim()));
        if (strHinhanh == null) {
            r.setHinhanh("NO AVATA");

        } else {
            r.setHinhanh(strHinhanh);
        }
        return r;
    }
