    public void setModel(Sachfr r) {
        int rowdata = tbbang.getSelectedRow();
        txtmSach.setText(r.getmSach());
        txttenSach.setText(r.gettSach());
        cbochuyenNganhs.setSelectedItem(r.getChuyennganhS());
        txtngayNhap.setText(date_Format.format(r.getNgaynhap()));
        txtsoLuong.setText(r.getSoluong() + "");
        txtgiaBan.setText(r.getGiaban() + "");
        //hinh anh
        if (r.getHinhanh().equalsIgnoreCase("NO AVATA")) {
            lblhinhanh.setText("NO AVATA");
            lblhinhanh.setIcon(null);

        } else {
            lblhinhanh.setText("");
            ImageIcon micon = new ImageIcon(getClass().getResource("/images/" + tbbang.getValueAt(rowdata, 7).toString()));
            Image img = micon.getImage();
            img.getScaledInstance(lblhinhanh.getWidth(), lblhinhanh.getHeight(), 0);
            lblhinhanh.setIcon(micon);

        }

    }
