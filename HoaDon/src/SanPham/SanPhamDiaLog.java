    private void tabsanphamMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabsanphamMouseClicked
        // TODO add your handling code here:
        SanPham sp = new SanPham();
        int rowdata = tabsanpham.rowAtPoint(evt.getPoint());
        txtdongia.setText(tabsanpham.getValueAt(rowdata, 3).toString());
        txtmasanpham.setText(tabsanpham.getValueAt(rowdata, 0).toString());
        txtmota.setText(tabsanpham.getValueAt(rowdata, 1).toString());
        txtsoluong.setText(tabsanpham.getValueAt(rowdata, 2).toString());
        txttensanpham.setText(tabsanpham.getValueAt(rowdata, 4).toString());
    }//GEN-LAST:event_tabsanphamMouseClicked

