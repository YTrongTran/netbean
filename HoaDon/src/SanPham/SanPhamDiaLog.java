    public SanPham getModel() {
        SanPham sp = new SanPham();
        sp.setDonGia(Double.parseDouble(txtdongia.getText()));
        sp.setMaSP(Integer.parseInt(txtmasanpham.getText()));
        sp.setMoTa(txtmota.getText());
        sp.setSoLuong(Integer.parseInt(txtsoluong.getText()));
        sp.setTenSP(txttensanpham.getText());
        return sp;
    }

    public void filldata() {
        DefaultTableModel model = (DefaultTableModel) tabsanpham.getModel();
        model.setRowCount(0);
        for (SanPham sp : daosp.getAllSanPham()) {
            Object rowdata[] = new Object[6];
            rowdata[0] = sp.getMaSP();
            rowdata[1] = sp.getMoTa();
            rowdata[2] = sp.getSoLuong();
            rowdata[3] = sp.getDonGia();
            rowdata[4] = sp.getTenSP();
            rowdata[5] = sp.getSoLuong() * sp.getDonGia();
            model.addRow(rowdata);
        }
    }
    private void btnthemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnthemActionPerformed
        // TODO add your handling code here:
        if (txtmasanpham.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "YÊU CẦU BẠN NHẬP ĐẦY ĐỦ");
        } else {
            SanPham sp = getModel();
            if (daosp.add(sp) > 0) {
                filldata();
                JOptionPane.showMessageDialog(this, "bạn thêm thành công");
            } else {
                JOptionPane.showMessageDialog(this, "bạn thêm thất bại");
            }
        }
    }//GEN-LAST:event_btnthemActionPerformed

