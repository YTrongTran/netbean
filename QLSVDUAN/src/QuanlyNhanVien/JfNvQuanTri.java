    private void btnthemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnthemActionPerformed
        // TODO add your handling code here:
        if (checkValiData()) {
            try {
                NhanVien nv = getModeL();
                if (daonv.add(nv) > 0) {
                    JOptionPane.showMessageDialog(this, "BẠN THÊM THÀNH CÔNG");
                    filldata();
                } else {
                    JOptionPane.showMessageDialog(this, "BẠN THÊM KHÔNG THÀNH CÔNG");
                }

            } catch (Exception e) {
                System.out.println("Error:" + e.toString());
            }
        } else {
            JOptionPane.showMessageDialog(this, "BAN CHUA NHAP DAY DU THEM OVER");
        }
    }//GEN-LAST:event_btnthemActionPerformed

    private void btnsuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsuaActionPerformed
        // TODO add your handling code here:
        if (txtmaNV.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "BẠN CHƯA CẬP NHẬT MÃ NHÂN VIÊN");
        } else {
            try {
                NhanVien nv = getModeL();
                if (daonv.edit(nv) > 0) {
                    JOptionPane.showMessageDialog(this, "BẠN ĐÃ CẬP NHẬT THÀNH CÔNG");
                    filldata();
                } else {
                    JOptionPane.showMessageDialog(this, "CẬP NHẬT THẤT BẠI");
                }
            } catch (Exception e) {
                System.out.println("Error:" + e.toString());
            }
        }
    }//GEN-LAST:event_btnsuaActionPerformed

    private void btnxoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnxoaActionPerformed
        // TODO add your handling code here:
        if (txtmaNV.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "BẠN CHƯA CẬP NHẬT MÃ NHÂN VIÊN ĐỂ XÓA");
        } else {
            try {
                NhanVien nv = getModeL();
                if (daonv.dell(nv) > 0) {
                    JOptionPane.showMessageDialog(this, "BẠN ĐÃ XÓA THÀNH CÔNG");
                    filldata();
                } else {
                    JOptionPane.showMessageDialog(this, "KHÔNG TÌM THẤY MÃ ID NHÂN VIÊN ĐỂ XÓA");
                }
            } catch (Exception e) {
            }
        }
    }//GEN-LAST:event_btnxoaActionPerformed

