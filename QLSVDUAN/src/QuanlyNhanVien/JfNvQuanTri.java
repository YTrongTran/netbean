    //hàm thêm
    //1/getModel
    public NhanVien getModeL() {
        NhanVien nv = new NhanVien();
        nv.setMaNV(txtmaNV.getText());
        nv.setMatKhau(txtmatkhau.getText());
        nv.setHoTen(txthovaten.getText());
        boolean cv = false;
        if (rdnhanvien.isSelected()) {
            cv = true;
        }
        nv.setVaitro(cv);
        if (txtmatkhau.getText().equals(txtxacnhanmk.getText())) {
            return nv;
        }
        return null;
    }

    public boolean checkValiData() {
        if (txtmaNV.getText().isEmpty()
                || txtmatkhau.getText().isEmpty()
                || txtxacnhanmk.getText().isEmpty()
                || txthovaten.getText().isEmpty()) {
            return false;
        }
        return true;
    }

    //ham filldata
    public void filldata() {
        DefaultTableModel model = (DefaultTableModel) tabbangnhanvien.getModel();
        model.setRowCount(0);
        for (NhanVien nv : daonv.getAllNhanVien()) {
            Object rowdata[] = new Object[4];
            rowdata[0] = nv.getMaNV();
            rowdata[1] = nv.getMatKhau();
            rowdata[2] = nv.getHoTen();
            String cv = "Trưởng Phòng";
            if (nv.isVaitro()) {
                cv = "Nhân Viên";
            }
            rowdata[3] = cv;
            model.addRow(rowdata);
        }
    }

    //hàm set()
    public void setModel(NhanVien nv) {
        txtmaNV.setText(nv.getMaNV());
        txtmatkhau.setText(nv.getMatKhau());
        txthovaten.setText(nv.getHoTen());
        rdnhanvien.setSelected(nv.isVaitro());
        rdtruongphong.setSelected(!nv.isVaitro());
    }
