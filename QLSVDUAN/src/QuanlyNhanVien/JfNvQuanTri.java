    public NhanVien getNhanVienAtPosition(int pos) {
        NhanVien nv = new NhanVien();
        nv.setMaNV(tabbangnhanvien.getValueAt(pos, 0).toString());
        nv.setMatKhau(tabbangnhanvien.getValueAt(pos, 1).toString());
        nv.setHoTen(tabbangnhanvien.getValueAt(pos, 2).toString());
        nv.setVaitro(tabbangnhanvien.getValueAt(pos, 3).toString().equalsIgnoreCase("Nhân Viên"));
        return nv;
    }
