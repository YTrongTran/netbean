    public SinhVien getModel() {
        SinhVien sv = new SinhVien();
        sv.setMaSV(Integer.parseInt(txtmasv.getText()));
        sv.setTenSV(txttensv.getText());
        ChuyenNganh n = (ChuyenNganh) cbomacn.getSelectedItem();
        sv.setMaCN(n);
        sv.setNgaySinh(DateHelps.toDate(txtngaysinh.getText()));
        boolean gt = false;
        if (rdnam.isSelected()) {
            gt = true;
        }
        sv.setGioiTinh(gt);
        sv.setPhone(txtphone.getText());
        return sv;
    }

    public void filldata() {
        DefaultTableModel model = (DefaultTableModel) tabbang.getModel();
        model.setRowCount(0);
        for (SinhVien sv : daos.getAllSinhVien()) {
            Object rowdata[] = new Object[6];
            rowdata[0] = sv.getMaSV();
            rowdata[1] = sv.getTenSV();
            int p = dao.findnhan(sv.getMaCN().getMaCN());
            rowdata[2] = dao.getAllChuyenNganh().get(p).getMaCN();;
            rowdata[3] = DateHelps.toString(sv.getNgaySinh());
            String gt = "Nữ";
            if (sv.isGioiTinh()) {
                gt = "Nam";
            }
            rowdata[4] = gt;
            rowdata[5] = sv.getPhone();
            model.addRow(rowdata);
        }
    }
