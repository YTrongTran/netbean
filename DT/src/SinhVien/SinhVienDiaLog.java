    private void tabbangMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabbangMouseClicked
        // TODO add your handling code here:
        SinhVien sv = new SinhVien();
        int position = tabbang.rowAtPoint(evt.getPoint());
        txtmasv.setText(tabbang.getValueAt(position, 0).toString());
        txttensv.setText(tabbang.getValueAt(position, 1).toString());
//        int p = dao.findnhan(tabbang.getValueAt(position, 2).toString());
        cbomacn.setSelectedIndex(dao.findnhan(Integer.parseInt(tabbang.getValueAt(position, 2).toString())));
        if (tabbang.getValueAt(position, 4).equals(false)) {
            rdnam.setSelected(true);
            rdnu.setSelected(false);
        } else {
            rdnam.setSelected(false);
            rdnu.setSelected(true);
        }
        txtngaysinh.setText(tabbang.getValueAt(position, 3).toString());
        txtphone.setText(tabbang.getValueAt(position, 5).toString());

    }//GEN-LAST:event_tabbangMouseClicked

