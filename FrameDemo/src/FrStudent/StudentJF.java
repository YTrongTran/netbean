public void getModel(Student st){
txtmSV.setText(st.getmSV());
txttenSV.setText(st.gettSV());
txtNgaysinh.setText(dateFormat.format(st.getNgaysinh()));
txtPhone.setText(st.getPhone());
boolean gt =st.isGioitinh();
    if (gt == false) {
        rdNu.isSelected();
    }
    txtEmail.setText(st.getEmail());
    

}
