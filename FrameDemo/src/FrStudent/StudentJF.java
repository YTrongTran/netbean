public Student getModel() throws ParseException{
Student st = new Student();
st.setmSV(txtmSV.getText());
st.settSV(txttenSV.getText());
st.setNgaysinh(dateFormat.parse(txtNgaysinh.getText()));
st.setPhone(txtPhone.getText());
st.setEmail(txtEmail.getText());
boolean gt = false;
    if (rdNam.isSelected()) {
        gt=true;
    }
    st.setGioitinh(gt);
    return st;

}
    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:

        try {
            Student st =getModel();
            if (dao.add(st)>0) {
                lblStatus.setText("Luu thanh cong");
                lblStatus.setForeground(Color.blue);
                lblCount.setText("COUNT: " + dao.count());
            }
        } catch (ParseException e) {
            System.out.println("Error:"+e.toString());
        }
    }//GEN-LAST:event_btnSaveActionPerformed

