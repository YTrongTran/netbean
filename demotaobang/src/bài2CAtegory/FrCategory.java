public boolean checkValidation(){
    if (TXTID.getText().isEmpty()||TXTNAME.getText().isEmpty()) {
        return false;
    }
    return true;
}

    private void BTNADDActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BTNADDActionPerformed
        // TODO add your handling code here:
        if (!checkValidation()) {
            JOptionPane.showMessageDialog(this, "valus is empty");
        }else{
            Category cate = new Category();
        cate.setId(Integer.parseInt(TXTID.getText()));
        cate.setName(TXTNAME.getText());
        
        
        if (cateDao.add(cate)>0) {
            System.out.println("add sucsseful ");
            System.out.println("count " +cateDao.count());
            lblCount.setText("COUNT: " +cateDao.count());
            lblStatus.setText("ADD Successful");
            lblStatus.setForeground(Color.BLUE);
            resertForm();
        }
            
        }
        
    }//GEN-LAST:event_BTNADDActionPerformed

    private void BTNDELActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BTNDELActionPerformed
        // TODO add your handling code here:
        int id = Integer.parseInt(TXTID.getText());
        if (cateDao.del(id)>=0) {
            lblStatus.setText("Deleted");
            lblCount.setText("Count: "+cateDao.count());
            lblStatus.setForeground(Color.blue);
        }else{
lblStatus.setText("Not Found");
lblStatus.setForeground(Color.red);
        }
    }//GEN-LAST:event_BTNDELActionPerformed

    private void BTNFINDActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BTNFINDActionPerformed
        // TODO add your handling code here:
        int id =Integer.parseInt(TXTID.getText());
        Category cate = cateDao.getCategoryByID(id);
        if (cate !=null) {
            lblStatus.setText("Found");
            TXTNAME.setText(cate.getName());
        }else{
        lblStatus.setText("Not Found");
        }
    }//GEN-LAST:event_BTNFINDActionPerformed

