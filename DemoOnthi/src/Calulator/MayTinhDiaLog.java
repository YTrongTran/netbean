public boolean ValicheckData(){
    if (txtsoa.getText().isEmpty()||txtsob.getText().isEmpty()||lblketqua.getText().isEmpty()) {
        return false;
    }
    return true;
}
    private void btntinhActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btntinhActionPerformed
        // TODO add your handling code here:
        if (ValicheckData()) {
          int a = Integer.parseInt(txtsoa.getText());
          int b = Integer.parseInt(txtsob.getText());
          int ketqua= 0;
            if (rdcong.isSelected()) {
                ketqua=a+b;
            }
            if (rdtru.isSelected()) {
                ketqua=a-b;
            }
            if (rdnhan.isSelected()) {
                ketqua=a*b;
            }
            if (rdchia.isSelected()) {
                ketqua=a/b;
            }
           lblketqua.setText("ket qua "+ketqua);
        }
    }//GEN-LAST:event_btntinhActionPerformed

