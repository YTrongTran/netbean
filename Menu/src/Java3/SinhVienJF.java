    private void btnsaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsaveActionPerformed
        // TODO add your handling code here:
        if (valicheckDate()) {
            try {
                SinhVien s = getModel();
                if (dao.add(s) > 0) {
                    JOptionPane.showMessageDialog(this, "BAN LUU KHONG DUNG");
                    lblstatus.setText("BAN LUU KHONG DUNG");
                    lblstatus.setForeground(Color.red);
                } else {
                    JOptionPane.showMessageDialog(this, "BAN LUU THANH CONG");
                    lblstatus.setText("BAN LUU THANH CONG");
                    lblstatus.setForeground(Color.blue);
                    filldata();
                }
            } catch (ParseException ex) {
                System.out.println("Error:" + ex.toString());
            }
        } else {
            JOptionPane.showMessageDialog(this, "BAN CHUA NHAP GIA TRI");
        }
    }//GEN-LAST:event_btnsaveActionPerformed

    private void btneditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btneditActionPerformed
        // TODO add your handling code here:

        if (txtmasinhvien.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "BAN CHUA NHAP GIA TRI VAO DE EDIT");
        } else {
            try {
                SinhVien s = getModel();
                if (dao.edit(s) < 0) {
                    JOptionPane.showMessageDialog(this, "BAN AN OKE DE CAP NHAT");
                    lblstatus.setText("BAN DANG CAP NHAT THANH CONG");
                    lblstatus.setForeground(Color.blue);
                    filldata();
                } else {
                    JOptionPane.showMessageDialog(this, "BAN EDIT THAT BAI");
                    lblstatus.setText("BAN KHONG TIM THAY");
                    lblstatus.setForeground(Color.red);
                }
            } catch (Exception ex) {
                System.out.println("Error:" + ex.toString());;
            }
        }
    }//GEN-LAST:event_btneditActionPerformed

    private void btnxoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnxoaActionPerformed
        // TODO add your handling code here:
        if (txtmasinhvien.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "BAN CHUA NHAP DAY DU");
        } else {
            try {
                SinhVien s = getModel();
                if (dao.del(s) < 0) {
                    JOptionPane.showMessageDialog(this, "VUI LONG CHON ẤN OKE DE TIEP TUC XOA");
                    JOptionPane.showMessageDialog(this, "BAN DA XOA THANH CONG");
                    lblstatus.setText("BAN DA XOA THANH CONG");
                    lblstatus.setForeground(Color.blue);
                    filldata();
                } else {
                    lblstatus.setText("KHONG TIM THAY YEU CAU CUA BAN");
                    lblstatus.setForeground(Color.red);
                }
            } catch (Exception e) {
                System.out.println("Erorr:" + e.toString());
            }
        }
    }//GEN-LAST:event_btnxoaActionPerformed

    private void tabbangMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabbangMouseClicked
        // TODO add your handling code here:
        int point = tabbang.rowAtPoint(evt.getPoint());
        String maSv = tabbang.getValueAt(point, 0).toString();
        SinhVien sv = dao.getNhanVienByID(maSv);
        if (sv == null) {
            return;
        }
        try {
            setModel(sv);
        } catch (Exception e) {
            System.out.println("No AVATA");
        }
    }//GEN-LAST:event_tabbangMouseClicked

