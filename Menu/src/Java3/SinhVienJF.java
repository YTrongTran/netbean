    public void reset() {
        txtmasinhvien.setText("");
        txttenSV.setText("");
        txtdate.setText("");
        rdnam.setSelected(true);
        txtdiachi.setText("");
        lblhinhanh.setText("HINH ANH");
        lblhinhanh.setIcon(null);
        strHinhanh = null;

    }

    public SinhVien getModel() throws ParseException {
        SinhVien s = new SinhVien();
        s.setTenNV(txttenSV.getText());
        s.setMaSV(txtmasinhvien.getText());
        s.setNgaySinh(date.parse(txtdate.getText()));
        boolean gt = false;
        if (rdnam.isSelected()) {
            gt = true;//nam
        }
        s.setGioiTinh(gt);
        s.setDiaChi(txtdiachi.getText());
        if (strHinhanh == null) {
            s.setHinhAnh("NO AVATA ");
        } else {
            s.setHinhAnh(strHinhanh);
        }
        return s;
    }

    public boolean valicheckDate() {
        if (txtmasinhvien.getText().isEmpty()
                || txttenSV.getText().isEmpty()
                || txtdate.getText().isEmpty()
                || txtdiachi.getText().isEmpty()) {
            return false;
        }
        return true;
    }

    //ham set
    public void setModel(SinhVien sv) {
        int rowdata = tabbang.getSelectedRow();
        txtmasinhvien.setText(sv.getMaSV());
        txttenSV.setText(sv.getTenNV());
        txtdate.setText(date.format(sv.getNgaySinh()));
        rdnam.setSelected(sv.isGioiTinh());
        rdnu.setSelected(!sv.isGioiTinh());
        txtdiachi.setText(sv.getDiaChi());
        if (sv.getHinhAnh().equalsIgnoreCase("NO AVATA")) {
            lblhinhanh.setText("NO AVATA");
            lblhinhanh.setIcon(null);
        } else {
            lblhinhanh.setText("");
            ImageIcon micon = new ImageIcon(getClass().getResource("/Images/" + tabbang.getValueAt(rowdata, 5).toString()));
            Image img = micon.getImage();
            img.getScaledInstance(lblhinhanh.getWidth(), lblhinhanh.getY(), 0);
            lblhinhanh.setIcon(micon);
        }
    }

    //ham do table 
    public void filldata() {
        DefaultTableModel tbmodel = (DefaultTableModel) tabbang.getModel();
        tbmodel.setRowCount(0);// ham xoa
        for (SinhVien s : dao.getAllSinhVien()) {
            Object rowdata[] = new Object[6];
            rowdata[0] = s.getMaSV();
            rowdata[1] = s.getTenNV();
            rowdata[2] = date.format(s.getNgaySinh());
            String gt = "Nữ";
            if (s.isGioiTinh()) {
                gt = "Nam";
            }
            rowdata[3] = gt;
            rowdata[4] = s.getDiaChi();
            rowdata[5] = s.getHinhAnh();
            tbmodel.addRow(rowdata);
        }
    }

