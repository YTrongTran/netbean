    public void filldata() {
        DefaultTableModel tbmodel = (DefaultTableModel) tabbang.getModel();
        tbmodel.setRowCount(0);
        for (Sinhvien sv : dao.getALlSinhvien()) {
            Object[] rowData = new Object[7];
            rowData[0] = sv.getMaSV();
            rowData[1] = sv.getTenSV();
            rowData[3]=sv.getEmail();
            rowData[2]=sv.getPhone();
            rowData[4]=sv.isGioitinh() ;
            rowData[5]=sv.getDiachi();
            rowData[6]=sv.getImages();
            tbmodel.addRow(rowData);
                                
                
        }

    }
