    public boolean validateForm() {
        StringBuilder sb = new StringBuilder();

        if (txtmasinhvien.getText().isEmpty()) {
            sb.append("ban chua nhap ma sinh vien \n");
            txtmasinhvien.setBackground(Color.red);
            JOptionPane.showMessageDialog(this, sb);
            return false;
        } else {
            txtmasinhvien.setBackground(Color.white);

        }
        if (txthoten.getText().isEmpty()) {
            sb.append("ban chua nhap ho va ten \n");
            txthoten.setBackground(Color.red);
            JOptionPane.showMessageDialog(this, sb);
            return false;
        } else {
            txthoten.setBackground(Color.white);
        }

        return true;

    }

    public Sinhvien getModel() {
        //get la return
        //set la void 
        Sinhvien sv = new Sinhvien();
        sv.setMaSV(txtmasinhvien.getText());
        sv.setTenSV(txthoten.getText());
        sv.setPhone(txtsdt.getText());
        sv.setEmail(txtemail.getText());

        boolean gt = false;
        if (rdnam.isSelected()) {
            gt = true;
        }
        sv.setGioitinh(gt);
        sv.setDiachi(txtdiachi.getText());
        return sv;
    }
